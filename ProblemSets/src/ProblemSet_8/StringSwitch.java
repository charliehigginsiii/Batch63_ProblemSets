package ProblemSet_8;

public class StringSwitch {
	public static void main(String[]args) {
		String string="Sixth Planet";
		switch(string) {
			case  "First Planet":
				System.out.println("Mercury");
				break;
			case  "Second Planet":
				System.out.println("Venus");
				break;
			case  "Third Planet":
				System.out.println("Earth");
				break;
			case  "Fourth Planet":
				System.out.println("Mars");
				break;
			case  "Fifth Planet":
				System.out.println("Mercury");
				break;
			case  "Sixth Planet":
				System.out.println("Venus");
				break;
			case  "Seventh Planet":
				System.out.println("Earth");
				break;
			case  "Eighth Planet":
				System.out.println("Mars");
				break;
			default:
				System.out.println("Pluto");
					
			
		}
	}

}
